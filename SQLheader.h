#ifndef SQLHEADER_H
#define SQLHEADER_H

// Include necessary Qt SQL libraries

#include <QSqlDatabase> // For QSqlDatabase class, represents a connection to a database
#include <QSqlDriver> // For QSqlDriver class, abstracts the database-specific details when accessing data
#include <QSqlError> // For QSqlError class, holds information about the last error occurred on a database
#include <QSqlQuery> // For QSqlQuery class, provides a means of executing and manipulating SQL statements
#include <QDebug> // For QDebug class, provides an output stream for debugging information
#include <QSqlTableModel> // For QSqlTableModel class, provides an editable data model for a single database table
#include <QFile> // For QFile class, provides an interface for reading from and writing to files

#endif // SQLHEADER_H